namespace Lampcms;


/**
 * Class responsible for creating
 * block with div with answers for
 * one question. Answers are found
 * in ANSWERS collection, pagination
 * is applied as necessary, sort order
 * will also be applied according to
 * param from Request
 *
 * @author Dmitri Snytkine
 *
 */
class Answers extends LampcmsObject
{
	/**
	 * How many answers to show per page
	 * @todo this should be in settings
	 *
	 * @var int
	 */
	protected $perPage = 10;

	/**
	 * Mongo cursor
	 *
	 * @var oject of type MongoCursor
	 */
	protected $oCursor;

	public function __construct(Registry $oRegistry){
		$this->oRegistry = $oRegistry;
	}


	/**
	 * Get html div with answers for this one question,
	 * sorted according to param passed in request
	 *
	 * Enclose result in <div> and add pagination to bottom
	 * of div if there is any pagination necessary!
	 *
	 * @param clsQuestion $oQuestion
	 *
	 * @param string desired format of result. Possible
	 * options are: html, array or json object
	 * This will be useful for when we have an API
	 *
	 *
	 * @return string html block
	 */
	public function getAnswers(Question $oQuestion, $result = 'html'){
		$qid = $oQuestion['_id'];
		$url = $oQuestion['url'];
		d('url: '.$url);
		$cond = $this->oRegistry->Request->get('cond', 's', 'i_ts');
		d('cond: '.$cond);
		/**
		 * Extra security validation,
		 * IMPORTANT because we should never use
		 * anything in Mongo methods directly from
		 * user input
		 */
		if(!in_array($cond, array('i_ts', 'i_score'))){
			throw new Exception('invalid value of param "cond"');
		}

		$where = array('i_qid' => $qid);
		$sort = array($cond => 1);

		$cursor = $this->oRegistry->Mongo->getCollection('ANSWERS')->find($where);
		d('$cursor: '.gettype($cursor));
		$cursor->sort($sort);
		$oPager = Paginator::factory($this->oRegistry);
		$oPager->paginate($cursor, $this->perPage,
		array('path' => $this->oRegistry->Ini->SITE_URL.'/questions/'.$qid.'/'.$url.'/'.$cond));

		$links = $oPager->getLinks();


		$func = null;
		$ownerId = $oQuestion['i_uid'];
		$showLink = (($ownerId > 0) && ($ownerId == $this->oRegistry->Viewer->getUid()) );

		d('adding accept link callback function');
		/**
		 * @todo Translate strings
		 */
		$accept = 'Accept';
		$alt = 'Click to accept this as best answer';
		$func = function(&$a) use ($accept, $alt, $showLink){
			/**
			 * Don't show Accept link for
			 * already accepted answer
			 */
			if(!($a['accepted'])){
				if($showLink){
					$a['accept_link'] = '<a class="accept" title="'.$alt.'" href="/accept/'.$a['_id'].'">'.$accept.'</a>';
				}
			} else {
				$a['accepted'] = '<img src="/images/accepted.png" alt="Best answer" title="Owner of the question accepted this as best answer">';
			}
		};
		
		/**
		 * Create div with answers, append pagination links
		 * to bottom and return the whole div block
		 */
		$answers = \tplAnswer::loop($cursor, true, $func).$links;

		return $answers;

	}
}
