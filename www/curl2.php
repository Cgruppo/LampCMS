echo ' $tempDir: '.$tempDir;

$cookieFile = $tempDir.'github.txt';

$version = curl_version();
$ssl_supported= ($version['features']);
echo '$ssl_supported: '.$ssl_supported;
echo ' flag: '.  CURL_VERSION_SSL;


$aHeaders = array(
'Accept' => 'text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8',
'Accept-Charset' => 'ISO-8859-1,utf-8;q=0.7,*;q=0.3',
'Accept-Language' => 'en-US,en;q=0.8',
'Cache-Control' => 'no-cache'
);

$body = null;
try{
	$oCurl = new \Lampcms\Curl();
	$oCurl->setHeaders($aHeaders)
	->setCookieFile($cookieFile);
	$oCurl->getDocument('https://github.com/login');
	$body = $oCurl->getResponseBody();

	//echo $body;
	//exit;

} catch (\Exception $e){

	$s = 'E: '.$e->getMessage().' in '.$e->getFile().' on '.$e->getLine();
	exit($s);
