echo ' $charset: '.$charset;
/*$info = $oCurl->getCurlInfo();
 print_r($info);*/

if(!$body){
	exit('something is wrong, no body');
}

echo __LINE__.' ';
$oHTML = new \Lampcms\String\HTMLString();
/**
 * Always use @ to suppress errors in loadHTML!
 * Unless html has previously been fixed with tidy!
 * 
 */
$res = @$oHTML->loadHTML($body);
echo 'loaded: '.$res;
//echo 'body: '.(string)$oHTML->saveHTML();
$forms = $oHTML->xpath('//form[@action = "/session"]');
if(1 !== $forms->length){
	exit('no login form or more than one');
}

$Form = $forms->item(0); // Lampcms\Dom\Element
$formUrl = $Form->getAttribute('action');
echo ' $formUrl: '.$formUrl.' ';

$inputs = $oHTML->xpath('//form[@action = "/session"]//input');
if($inputs->length < 2){
	exit('No form inputs found');
}

$aInputs = array();
for($i = 0; $i < $inputs->length; $i+= 1){
	$item = $inputs->item($i);
	$name = $item->getAttribute('name');
	$aInputs[$name] = $item->getAttribute('value');
}

echo ' $aInputs : '.print_r($aInputs, 1);

$aInputs['login'] = 'wer';
$aInputs['password'] = 'werwe2t';


function get_page($url, $ref = ''){
	global $aHeaders;
	global $cookieFile;
	$oCurl = new \Lampcms\Curl();
	$oCurl->setHeaders($aHeaders)
	->setCookieFile($cookieFile)
	->setReferrer($ref)
	->getDocument($url);

	$body = $oCurl->getResponseBody();

	echo ' body: '.$body;
}




try{
	$oCurl = new \Lampcms\Curl();
	$oCurl->setHeaders($aHeaders)
	->setCookieFile($cookieFile)
	->setReferrer('https://github.com/login');
	$oCurl->getDocument('https://github.com'.$formUrl, null, null, array('formVars' => $aInputs, 'redirect' => 4));

	$body = $oCurl->getResponseBody();

	echo ' body: '.$body;

} catch (\Exception $e){

	$info = $oCurl->getCurlInfo();
	echo 'INFO: '.print_r($info, 1);

	if($e instanceof \Lampcms\HttpRedirectException){
		$newUrl = $e->getNewURI();
		get_page($newUrl, $info['url']);
	}
	$s = 'E: '.$e->getMessage().' in '.$e->getFile().' on '.$e->getLine();
	exit($s);
}
